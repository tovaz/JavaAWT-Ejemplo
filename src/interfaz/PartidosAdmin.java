    private String SelectedId;
    private void jtpartidosMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtpartidosMousePressed
        JTable tabla =(JTable) evt.getSource();
        Point punto = evt.getPoint();
        int row = tabla.rowAtPoint(punto);

        SelectedId = (String)tabla.getValueAt(row, 0);
        if (evt.getClickCount() == 2 && tabla.getSelectedRow() != -1)
        mostrarInfo("info");
    }//GEN-LAST:event_jtpartidosMousePressed

    public void mostrarInfo(String boton) { 
        manejadorPronosticos mPronostico = new manejadorPronosticos();
        partido _partido = new partido();
        _partido = (partido) mPartidos.buscarPorID(SelectedId).datos;
        _partido.setFaseGrupo(boton);
        
        if (!boton.equals("info") ){ 
            if (mPronostico.existePronostico(_partido, padre.usuarioLogeado) ){
                JOptionPane.showMessageDialog(this, "No puede volver a pronosticarlo, solo editar el pronostico ya hecho.");
                return;
            }
                
            
            if (!_partido.isFinalizado())
                new partidoInfo(this.padre,true,_partido, padre.usuarioLogeado).setVisible(true);
            else
                JOptionPane.showMessageDialog(this, "El partido ya se a jugado, no puedo pronosticar/editar un partido que ya se jugo.");
        }
        else
            new partidoInfo(this.padre,true,_partido, padre.usuarioLogeado).setVisible(true);
    }
    
    private TableRowSorter trsFiltro;
    private void filtro()
    {
        
        int colBuscar = 0;
        String tb = "";
        if (!cbclasificacion.getSelectedItem().equals("FaseGrupos") || cbgrupos.getSelectedItem().equals("Ver Todos")){
            colBuscar = 1;
            tb = cbclasificacion.getSelectedItem().toString();
        }
        else{
            colBuscar = 3;
            tb = cbgrupos.getSelectedItem().toString();
        }
        
        //JOptionPane.showMessageDialog(null, tb);
        trsFiltro.setRowFilter(RowFilter.regexFilter(tb, colBuscar));
    }
    
