    private void cbclasificacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbclasificacionActionPerformed
            lgrupos.setVisible(cbclasificacion.getSelectedItem().equals("FaseGrupos"));
            cbgrupos.setVisible(cbclasificacion.getSelectedItem().equals("FaseGrupos"));
            trsFiltro = new TableRowSorter(jtpartidos.getModel());
            jtpartidos.setRowSorter(trsFiltro);
            
            if (!cbclasificacion.getSelectedItem().equals("Ver Todos"))
                filtro();
                
    }//GEN-LAST:event_cbclasificacionActionPerformed

