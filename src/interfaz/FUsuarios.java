    private void tbuscarKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tbuscarKeyTyped
        tbuscar.addKeyListener(new KeyAdapter() {
            public void keyReleased(final KeyEvent e){
                String cadena = tbuscar.getText();
                tbuscar.setText(cadena);
                repaint();
                filtro();
            }
        });
        
        trsFiltro = new TableRowSorter(jtusuarios.getModel());
        jtusuarios.setRowSorter(trsFiltro);
    }//GEN-LAST:event_tbuscarKeyTyped

