    private void EditarUsuario()
    {
        Usuario usr = (Usuario)mUsuarios.buscarPorID(SelectedId).datos;
        if (usr != null)
            new JDEditarU(this.padre,true, usr).setLocationRelativeTo(this);
        else
            JOptionPane.showMessageDialog(null, "Error no ha seleccionado ningun usuario.");
        
        this.actualizarTabla();
    }
    
    String SelectedId = "";
    private void jtusuariosMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtusuariosMousePressed
        JTable tabla =(JTable) evt.getSource();
        Point punto = evt.getPoint();
        int row = tabla.rowAtPoint(punto);
        
        SelectedId = (String)tabla.getValueAt(row, 0);
        if (evt.getClickCount() == 2 && tabla.getSelectedRow() != -1)
            this.EditarUsuario();
    }//GEN-LAST:event_jtusuariosMousePressed

    PronosticosAdmin pronosticoAdmin;
    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        Administrador adminPanel = (Administrador) padre;
        manejadorUsuario mUsuario = new manejadorUsuario();
        Usuario usr = (Usuario) mUsuario.buscarPorID(SelectedId).datos;
        if (pronosticoAdmin == null || !pronosticoAdmin.isVisible()) // PARA NO ABRIR VARIOS FORMULARIOS DE PRONOSTICOS
        {
            pronosticoAdmin = new PronosticosAdmin(adminPanel);
            pronosticoAdmin.setResizable(true);
            pronosticoAdmin.setMaximizable(true);
            pronosticoAdmin.setIconifiable(true);
            pronosticoAdmin.setClosable(true);
            adminPanel.agregarForm(pronosticoAdmin);
        }
        
        pronosticoAdmin.filtrarporNickname(usr.getNickname());
        pronosticoAdmin.setVisible(true);
        pronosticoAdmin.setFocusable(true);
    }//GEN-LAST:event_jButton4ActionPerformed
    
    private TableRowSorter trsFiltro;
    private void filtro()
    {
        int colBuscar = 0;
        colBuscar = cbFiltro.getSelectedIndex()+1;
        if (cbFiltro.getSelectedItem() == "Privilegios")
            colBuscar = 8;
        trsFiltro.setRowFilter(RowFilter.regexFilter(tbuscar.getText(), colBuscar));
    }

